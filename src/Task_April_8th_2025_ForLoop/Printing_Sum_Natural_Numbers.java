package Task_April_8th_2025_ForLoop;

public class Printing_Sum_Natural_Numbers
    {
        public static void main(String[] args)
        {
            int sum = 0;
            for (int i = 0; i <= 10; i++)
            {
                sum = sum + i;

//                System.out.println("The Sum of Natural Number is : " + sum);
            }
            System.out.println("The Sum of Natural Number is : " + sum);
        }
}
