package Task_April_8th_2025_ForLoop;

public class Printing_Evennumbers_1to20
    {
        public static void main(String[] args)
        {
            for (int i = 1; i <=20; i++)
            {
                if (i%2==0)
                {
                    System.out.println(i);
                }
            }
        }
}
