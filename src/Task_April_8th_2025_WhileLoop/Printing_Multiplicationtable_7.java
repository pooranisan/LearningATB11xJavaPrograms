package Task_April_8th_2025_WhileLoop;

public class Printing_Multiplicationtable_7
    {
        public static void main(String[] args)
        {
            int table = 7;
            int i = 1;

            while(i <= 10)
            {
                System.out.println(table + "*" + i + "=" + (table*i));
                i++;
            }
        }
}
