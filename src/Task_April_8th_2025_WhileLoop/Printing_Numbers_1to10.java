package Task_April_8th_2025_WhileLoop;

public class Printing_Numbers_1to10
    {
        public static void main(String[] args)
        {
            int i =1;
            System.out.println("Printing numbers from 1 to 10");

            while(i<= 10)
            {
                System.out.println(i);
                i++;
            }
        }
}
