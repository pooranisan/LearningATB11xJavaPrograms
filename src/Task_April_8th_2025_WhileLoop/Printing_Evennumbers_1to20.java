package Task_April_8th_2025_WhileLoop;

public class Printing_Evennumbers_1to20
    {
        public static void main(String[] args)
        {
            int i = 1;
            while (i <=20)
            {
                if (i%2 == 0)
                {
                    System.out.println(i);

                }
                i++;
            }
        }
}
