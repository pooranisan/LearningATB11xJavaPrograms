package ex_02_Java_Basics_part2;

public class Lab015_Printing_table9_For_loop {

    public static void main(String[] args) {


        System.out.printf("Printing Table 9 \n");

        int number=9;

        for (int i=1; i<=10; i++)
        {
            System.out.println(number + "*" +i + "=" + number * i);


        }

    }
}
