package ex_13_Stringbuilder_Stringbuffer_functions;

public class Lab_63_StringBuilder_Buffer
        {
            public static void main(String[] args)
            {
                String a = "Poorani";
                String b = new String ("Chandiran");

                StringBuffer stringBuffer = new StringBuffer("Poorani");
                StringBuilder stringBuilder = new StringBuilder("Poorani");

                System.out.println(a);
                System.out.println(b);
                System.out.println(stringBuffer);
                System.out.println(stringBuilder);
            }
}
