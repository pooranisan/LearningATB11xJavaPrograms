package ex_10_DoWhile;

public class Lab_47_Do_While
{

    public static void main(String[] args)
    {
        int a =1;
        do
        {
            System.out.println("This is body, which will execute 1 time");
            System.out.println(a);
            a++;
        }
        while(a < 10);

    }
}
